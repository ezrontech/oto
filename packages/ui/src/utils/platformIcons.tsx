/**
 * Get platform info by name or URL
 */
export function getPlatformInfo(platformOrUrl: string): PlatformInfo {
    const normalized = platformOrUrl.toLowerCase().trim();

    // Try exact match first
    if (PLATFORM_DATABASE[normalized]) {
        return PLATFORM_DATABASE[normalized];
    }

    // Try to detect from URL
    for (const [key, platform] of Object.entries(PLATFORM_DATABASE)) {
        if (platform.urlPattern && platform.urlPattern.test(platformOrUrl)) {
            return platform;
        }
    }

    // Default to website icon
    return PLATFORM_DATABASE.website;
}

